import turtle as t
pen = t.Turtle()
t.bgcolor('#000000')
t.delay(8)
pen.color('#FF2400')
pen.begin_fill()
pen.left(40)
pen.forward(160)
pen.circle(87, 190)
pen.right(100)
pen.circle(87, 190)
pen.forward(160)
pen.left(100)
pen.end_fill()
def txt():
    pen.up()
    pen.setpos(-70, 140)
    pen.color('#FFFFFF')
    pen.write('ya u sebya molodec', font=("Comic Sans MS", 16))
txt()
pen.end_fill()
t.exitonclick()
